function textAnimation() {
    var x = document.getElementById("border-colour");
    var opacity = 0;
    function fade(){
        x.style.opacity = opacity;
        opacity += 0.01;
        if (opacity >= 1){
            x.style.opacity = 1;
            return true;
        }
    window.requestAnimationFrame(fade);  
    }
    fade();

    }

